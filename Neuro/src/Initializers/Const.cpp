#include "Initializers/Const.h"
#include "Tensors/Tensor.h"

namespace Neuro
{
	//////////////////////////////////////////////////////////////////////////
	Const::Const(float value)
	{
		m_Value = value;
	}

	//////////////////////////////////////////////////////////////////////////
	void Const::Init(Tensor& t)
	{
        t.FillWithValue(m_Value);
	}
}
