#include "Initializers/Constant.h"
#include "Tensors/Tensor.h"

namespace Neuro
{
	//////////////////////////////////////////////////////////////////////////
	Constant::Constant(float value)
	{
		m_Value = value;
	}

	//////////////////////////////////////////////////////////////////////////
	void Constant::Init(Tensor& t)
	{
        t.FillWithValue(m_Value);
	}
}
